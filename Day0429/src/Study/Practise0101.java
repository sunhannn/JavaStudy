package Study;

public class Practise0101 {

	public static void main(String[] args) {
		
		//변수 선언
		int i, j;// i : 단, j : 내부터에서 1~9까지 반복처리
		
		for (i =2;i<10;i++) {
			
			for (j =1; j<=9;j++) {
				System.out.printf("%d * %d = %d\t",i,j,i*j);	
				
			}
			
			System.out.println();
			
		}
		
		
		
		

	}

}
