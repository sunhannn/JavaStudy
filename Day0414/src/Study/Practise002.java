package Study;

public class Practise002 {

	public static void main(String[] args) {
		

		/*
		 *  증강 연산자(++/--) : 하나씩 증가시키거나, 감소 시키는 연산
		 * 
		 * 
		 * 
		 * 
		 * 
		 */
		
		int x = 10;
		int y = x++;
		//전 대입 후 연산, 연산자가 앞에 있는 것과 값이 다르게 나옴
		
		System.out.println("x : "+x+", y : "+y);
		
		
		
		
	}

}
