package Study;

public class Operator007 {

	public static void main(String[] args) {
		
		int a =10;
		int result = 0;
		
		result += a;
		System.out.println("result = "+result);
		result -= a;
		System.out.println("result = "+result);
		result *= a;	
		System.out.println("result = "+result);
		result %= a;
		System.out.println("result = "+result);
		result /= a;
		System.out.println("result = "+result);

	}

}
