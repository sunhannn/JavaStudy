package Study;

public class Operator001 {

	public static void main(String[] args) {
		
		
		
		
		
		/*
		 *   "*" : 곱하기 역할도 하지만, 모든 문자를 나타내는 만능문자 이기도 함
		 *   / : 몫을 구한다.
		 *   % : 나머지를 구한다.
		 *   
		 *   쉬프트 연산자
		 *   >> : /2 나누기 2
		 *   << : *2 곱하기 2
		 *   
		 * 
		 */
		int i = 7, j = 5;
		
		//+, -, *, / ,
		System.out.println(i + " + " +j+ " = " +(i+j));
		System.out.printf("%d / %d = %d%n", i, j, (i % j));
		
	}

}
